public class forArrays {
    public static void main(String[] args){

    // Definindo um arrays de numeros inteiros
    int[] numeros = {10, 20, 30, 40, 50};

    // variavel para armazenar a soma dos elementos do array
    int soma = 0;

    // loop for para iteirar sobre os elementos
    for (int i = 0; i < numeros.length; i++){
        // Adicionando o valor do elemento atual ao total
        soma += numeros[i];
    }

    // Calculando a media dos elementos

    }
}
